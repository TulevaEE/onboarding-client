export const StatusAlert = ({
  type = 'success',
  className,
  title,
  actions,
  children,
}: StatusAlertProps) => {
  const classes = [styles.statusAlert, className].filter(Boolean).join(' ');

  const renderTitle = (value: ReactNode) => {
    if (value === undefined || value === null) {
      return null;
    }

    if (typeof value === 'string') {
      return <h2 className={styles.statusAlert__heading}>{value}</h2>;
    }

    return value;
  };

  return (
    <div className={classes}>
      <div className={styles.statusAlert__header}>
        <span className={styles.statusAlert__icon} aria-hidden="true">
          {icons[type]}
        </span>
        {renderTitle(title)}
      </div>
      {children ? <div className={styles.statusAlert__body}>{children}</div> : null}
      {actions ? (
        <div className={`${styles.statusAlert__actions} col-12 col-sm-6`}>{actions}</div>
      ) : null}
    </div>
  );
};
